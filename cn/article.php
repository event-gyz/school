                                    <h4>01<br>分离练习</h4>
                                    <p><b>大多数初次进入同侪团体的孩子，可能会面对分离焦虑。</b>这是孩子离开安全的环境和重要他人时，自然发生的焦虑情绪。因为对外界有好奇，对新鲜事物有渴望，但又偏偏对于陌生环境害怕，因而产生矛盾。通过重复确认、观察父母的情绪表现，孩子会学习是否能安全区探索。父母可以通过陪伴、鼓励示范和耐心等待，以及不断与孩子沟通了解他的焦虑，去减缓分离的不适感，进而帮助孩子面对环境变化的适应能力。</p>
                                    <p><b>分离适应练习可以增强孩子日后适应环境能力，对于事物探索的开放程度。</b></p>
                                    <h4>02<br>团队合作</h4>
                                    <p>团体活动当中，孩子将面临许多规矩。有些是师长订定的规矩，有些是孩子内部自己订定的游戏规矩。这些都会冲击到孩子原本在家的习惯。尤其是家里没有相同年纪的孩子，他们更会对团队活动感觉陌生。<b>大人可以适时介入和引导，让孩子练习在团队中合作、等待、分工</b>。当孩子渐渐体验到合作可以带来的效益时，可以培养孩子对于他人存在的好感，了解自己能够成为团队一份子的责任感，进而发展对团队的归属感。</p>
